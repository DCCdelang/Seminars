def function_2b(string_1, string_2):
    '''
    takes in 2 strings. first string gets lower cased, second one uppercased.
    return dictionary with first string lower cased under L, second string upper cased under U and combined under C.
    '''
    lower = string_1.lower()
    upper = string_2.upper()
    combined = string_1 + string_2

    dict = {"L": lower,
               "U": upper,
               "C": combined}

    return dict
