def function_2c(w, x, y, z):
    """
    takes 4 inputs (w,x,y,z)
    multiplies x and y
    does a division of x and y
    adds w and z
    subtracts w and z
    returns dict of all results with as key the name of the operation performed
    """
    multiplication = x * y
    division = x / y
    addition = w + z
    subtraction = w - z

    results = {"multiply": multiplication,
               "divide": division,
               "add": addition,
               "subtract": subtraction}

    return results
