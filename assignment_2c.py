def function_2c(w, x, y, z):
    """
    Argument:
    w, x, y, z -- numbers for calculations

    Returns:
    results -- dictionary with:
            1. x multiplied with y
            2. x divided by y
            3. w and z added together
            4. w minus z
    """

    multiplication = x * y
    division = x / y
    addition = w + z
    subtraction = w - z

    results = {"multiply": multiplication,
               "divide": division,
               "add": addition,
               "subtract": subtraction}

    return results
